#include<stdio.h>

/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: end result
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substract two integers
 * @a: first integer
 * @b: second integer
 * Return: Substractions end result
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - modulo of two integers
 * @a: first integer
 * @b: second integer
 * Return: modulus end result
 */

int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: end
 */

int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: division results
 */

int div(int a, int b)
{
	return (a / b);
}
